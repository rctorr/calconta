<?php
  function resta($op1, $op2 ){
    /*descripción: resta el valor de op1 con op2 y regresa el resultado
        parámetros:
        op1 Es el primer operando de la resta
        op2 Es el segundo operando de la resta

      Valor de regreso
        regresa un número o resultado de la resta de op1+op2
    */

    $resultado = $op1 - $op2;
    return $resultado;

  }


 ?>
