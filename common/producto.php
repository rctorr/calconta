
<?php
        /* Descripción: multiplica los numeros pr1 con pr2 y regresa el resultado*/

    function producto($pr1, $pr2) {
            
              
        /*
        Descripción: Suma los numeros op1 con op2 y regresa el                              resultado
        Parametros:
        op1     Es el primer operando de la suma
        op2     Es el segundo operando de la suma
        
        Valor de regreso:
            Regresa un numero resultado de la suma de op1+op2
        */
        
        $resultado = $pr1 * $pr2;
        return $resultado;
        
    }
    
