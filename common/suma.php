<?php

  function suma($op1, $op2) {
    /*
      Descripción: Suma el valor de los números op1 con op2 y regresa el
                   resultado.
      Parámetros:
      op1   Es el primer operando de la suma
      op2   Es el segundo operando de la suma

      Valor de regreso:
         Regresa un número resultado de la suma de op1+op2
    */

    $resultado = $op1 + $op2;

    return $resultado;
  }

?>
